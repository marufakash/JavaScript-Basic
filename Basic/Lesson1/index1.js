// number to string - tostring()

let num1 = 25;
num1 = toString(num1);
console.log(typeof(num1));

// string to number - Number(), parseInt(), parseFloat()
let num2 = "88";
num2 = Number(num2);
console.log(typeof(num2));

// toFixed()
let num3 = 22.83729;
console.log(num3.toFixed(2));

// toPrecision
console.log(num3.toPrecision(4));